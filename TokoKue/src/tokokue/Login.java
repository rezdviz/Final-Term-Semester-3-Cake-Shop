    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        try {
            sql = "SELECT * FROM tblogin WHERE username='"+txt_user.getText()+"' AND password='"+txt_pass.getText()+"'";
            rs = stat.executeQuery(sql);
            if (rs.next()) {
                if (txt_user.getText().equals(rs.getString("username")) && txt_pass.getText().equals(rs.getString("password"))) {
                    JOptionPane.showMessageDialog(null, "Anda berhasil Login");
                    new MenuUtama().setVisible(true);
                    this.setVisible(false);
                }
            }
            
            sql = "SELECT * FROM tbsignup WHERE username='"+txt_user.getText()+"' AND password='"+txt_pass.getText()+"'";
            rs = stat.executeQuery(sql);
            if(rs.next()) {
                if (txt_user.getText().equals(rs.getString("username")) && txt_pass.getText().equals(rs.getString("password"))) {
                    JOptionPane.showMessageDialog(null, "Anda berhasil Login");
                    new MenuUtama().setVisible(true);
                    this.setVisible(false);
                }
            }
                else {
                JOptionPane.showMessageDialog(null, "Username atau Password salah");
                kosong();
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal login");
        }
    }//GEN-LAST:event_loginActionPerformed

